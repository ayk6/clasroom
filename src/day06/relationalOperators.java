package day06;

public class relationalOperators {
    public static void main(String[] args) {
        //karşılaştırma operatörleri
        //== karşılaştırma (Comparison) operatörü //boolean ile çalışır
        //eşitlik işlemidir, true ya da false sonucu verir
        boolean num=2+2==4;
        System.out.println(num); //true

        // != eşit değildir operatörü

        boolean num2=5!=3+2;
        System.out.println(num2); //false

         //> büyüktür , >= büyük eşittir
        // <küçüktür , <=küçük eşittir




    }
}
